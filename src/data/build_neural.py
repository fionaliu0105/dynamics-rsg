def canvas_len(cfg: Config, bin_ms: float) -> int:
    """Number of neural time bins = the model's fixed canvas at ``bin_ms`` resolution."""
    return int(round(cfg.total_time / bin_ms))


def _pick_column(columns: Sequence[str], key: str, required: bool = True) -> Optional[str]:
    """Resolve a logical field (e.g. ``prior``) to the real column present in the data."""
    for cand in _COL_CANDIDATES[key]:
        if cand in columns:
            return cand
    if required:
        raise KeyError(
            f"could not find a {key!r} column; tried {_COL_CANDIDATES[key]}; "
            f"trial_info has {list(columns)}. Add the real name to _COL_CANDIDATES."
        )
    return None


def _col_ms(df, name: str) -> np.ndarray:
    """Return column ``name`` as float milliseconds, handling timedelta or s/ms numerics."""
    s = df[name].to_numpy()
    if np.issubdtype(s.dtype, np.timedelta64):
        return s / np.timedelta64(1, "ms")
    s = s.astype(float)
    finite = s[np.isfinite(s)]
    if finite.size and np.nanmax(np.abs(finite)) < 10.0:  # values look like seconds
        s = s * 1000.0
    return s


def _label_effector(df, col: str) -> np.ndarray:
    """Map the effector column to canonical 'eye'/'hand' strings."""
    vals = df[col].to_numpy()
    if col in ("is_eye", "is_saccade"):
        return np.where(vals.astype(bool), "eye", "hand")
    out = np.array([str(v).lower() for v in vals], dtype=object)
    # Accept already-canonical or common synonyms.
    synonym = {"saccade": "eye", "eye": "eye", "hand": "hand", "joystick": "hand"}
    return np.array([synonym.get(v, v) for v in out], dtype=object)


def _label_prior(df, col: str) -> np.ndarray:
    """Map the prior column to canonical 'short'/'long' strings."""
    vals = df[col].to_numpy()
    if col in ("is_short", "is_short_block"):
        return np.where(vals.astype(bool), "short", "long")
    out = np.array([str(v).lower() for v in vals], dtype=object)
    synonym = {"short": "short", "long": "long", "0": "short", "1": "long"}
    return np.array([synonym.get(v, v) for v in out], dtype=object)


def resolve_trials(trial_info) -> Dict[str, np.ndarray]:
    """Normalize a raw ``trial_info`` DataFrame into per-trial arrays + a valid mask.

    Pure (no nlb_tools). Returns a dict with, per trial: ``cond_idx`` (canonical 0..19
    or -1 if invalid), ``direction`` (int label), ``tp`` (ms), and ``keep`` (bool mask
    of trials that map to a real Condition). Verifies ts/effector/prior against
    ``src.conditions`` and raises if the schema does not line up.
    """
    cols = list(trial_info.columns)
    c_eff = _pick_column(cols, "effector")
    c_prior = _pick_column(cols, "prior")
    c_ts = _pick_column(cols, "ts")
    c_tp = _pick_column(cols, "tp")
    c_dir = _pick_column(cols, "direction", required=False)
    c_out = _pick_column(cols, "outlier", required=False)

    effector = _label_effector(trial_info, c_eff)
    prior = _label_prior(trial_info, c_prior)
    ts_ms = _col_ms(trial_info, c_ts)
    tp_ms = _col_ms(trial_info, c_tp)
    n = len(effector)

    if c_dir is not None:
        raw_dir = trial_info[c_dir].to_numpy()
        _, direction = np.unique(raw_dir, return_inverse=True)  # 0..(D-1)
    else:
        log.warning("no direction column found; treating all trials as one direction")
        direction = np.zeros(n, dtype=int)

    outlier = (
        trial_info[c_out].to_numpy().astype(bool)
        if c_out is not None
        else np.zeros(n, dtype=bool)
    )

    cond_idx = np.full(n, -1, dtype=int)
    for i in range(n):
        if outlier[i] or not np.isfinite(ts_ms[i]):
            continue
        try:
            cond = Condition(str(prior[i]), int(round(ts_ms[i])), str(effector[i]))
        except ValueError:
            continue  # (prior, ts, effector) not a real cell (e.g. aborted trial)
        cond_idx[i] = condition_index(cond)

    keep = cond_idx >= 0
    _verify_schema(prior[keep], ts_ms[keep], effector[keep])
    log.info("resolved %d/%d trials into %d conditions", int(keep.sum()), n, N_CONDITIONS)
    return {"cond_idx": cond_idx, "direction": direction, "tp": tp_ms, "keep": keep}


def _verify_schema(prior: np.ndarray, ts_ms: np.ndarray, effector: np.ndarray) -> None:
    """Assert the resolved labels match ``src.conditions`` (fail loud, fail early)."""
    got_eff = set(map(str, effector))
    if not got_eff <= set(EFFECTORS) or not set(EFFECTORS) <= got_eff:
        raise ValueError(f"effector labels {got_eff} != schema {set(EFFECTORS)}")
    got_priors = set(map(str, prior))
    if not set(PRIORS) <= got_priors:
        raise ValueError(f"missing a prior: got {got_priors}, need {set(PRIORS)}")
    for p in PRIORS:
        sel = prior == p
        got_ts = set(int(round(v)) for v in ts_ms[sel])
        if not got_ts <= set(TS_BY_PRIOR[p]):
            raise ValueError(
                f"{p} prior has ts {sorted(got_ts)} outside schema {TS_BY_PRIOR[p]}"
            )


def _fill_time_nans(states: np.ndarray) -> np.ndarray:
    """Forward/back-fill NaN bins along time per (condition, unit) so output is finite.

    Late tail bins can be all-NaN when no trial in a condition reached that far; hold
    the last observed rate forward (and back-fill any leading gap) rather than emit NaN,
    which the preprocessor rejects.
    """
    out = np.array(states, dtype=float)
    n_cond, T, U = out.shape
    for c in range(n_cond):
        for u in range(U):
            col = out[c, :, u]
            finite = np.where(np.isfinite(col))[0]
            if finite.size == 0:
                col[:] = 0.0
                continue
            first, last = finite[0], finite[-1]
            col[:first] = col[first]
            col[last + 1:] = col[last]
            # interior gaps: linear interp over the finite support
            if finite.size < (last - first + 1):
                idx = np.arange(first, last + 1)
                col[first:last + 1] = np.interp(idx, finite, col[finite])
            out[c, :, u] = col
    return out


def average_conditions(
    arrays: Sequence[np.ndarray],
    cond_idx: np.ndarray,
    direction: np.ndarray,
    indices: Sequence[int],
) -> np.ndarray:
    """Direction-balanced condition average over the given trial ``indices``.

    Pure numpy. ``arrays[i]`` is a per-trial ``[T, U]`` rate array (may contain NaN from
    out-of-range bins). For each canonical condition we average within each target
    direction first, then average the direction-means -> marginalizing direction with
    equal weight (robust to the sampled prior's direction imbalance). Returns
    ``[20, T, U]``, NaN-filled to finite.
    """
    T, U = np.asarray(arrays[0]).shape
    out = np.full((N_CONDITIONS, T, U), np.nan, dtype=float)
    idx = np.asarray(indices)
    for c in range(N_CONDITIONS):
        in_c = idx[cond_idx[idx] == c]
        if in_c.size == 0:
            continue
        dir_means = []
        for d in np.unique(direction[in_c]):
            in_cd = in_c[direction[in_c] == d]
            stack = np.stack([np.asarray(arrays[i], dtype=float) for i in in_cd], axis=0)
            dir_means.append(np.nanmean(stack, axis=0))
        out[c] = np.nanmean(np.stack(dir_means, axis=0), axis=0)
    return _fill_time_nans(out)


def stratified_splits(
    cond_idx: np.ndarray, indices: Sequence[int], n_splits: int, rng: np.random.Generator
) -> List[np.ndarray]:
    """Partition trial ``indices`` into ``n_splits`` disjoint sets, stratified by condition.

    Stratifying by condition keeps every condition populated in every split, so each
    split's condition-average (the RSA noise-ceiling input) is well-defined.
    """
    parts: List[List[int]] = [[] for _ in range(n_splits)]
    idx = np.asarray(indices)
    for c in range(N_CONDITIONS):
        in_c = idx[cond_idx[idx] == c]
        in_c = rng.permutation(in_c)
        for s, chunk in enumerate(np.array_split(in_c, n_splits)):
            parts[s].extend(chunk.tolist())
    return [np.asarray(sorted(p)) for p in parts]


def build_input_tensor(cfg: Config, bin_ms: float) -> np.ndarray:
    """The 3-channel external drive [20, T, 3] on the neural canvas.

    Reuses the model's OWN generator so the encoding + scale are identical to the
    stored model inputs (Ready/Set pulses on ch0, tonic prior/effector context on
    ch1/ch2), which is what ``idsa.input_dsa`` requires (matching input dim & scale).
    """
    from src.task.rsg import build_trial

    ncfg = replace(cfg, dt=float(bin_ms))
    inp = np.stack([build_trial(ncfg, c, jitter=False)[0][0] for c in CONDITIONS], axis=0)
    return inp.astype(float)


def condition_event_bins(cfg: Config, bin_ms: float, mean_tp_ms: Dict[str, float]) -> Dict:
    """Per-condition Ready/Set/Go bin indices for the future event_anchored warp."""
    ready = int(round(cfg.ready_onset / bin_ms))
    out = {}
    T = canvas_len(cfg, bin_ms)
    for c in CONDITIONS:
        set_bin = ready + int(round(c.ts / bin_ms))
        tp = mean_tp_ms.get(c.key, float(c.ts))
        go_bin = min(T - 1, ready + int(round((c.ts + tp) / bin_ms)))
        out[c.key] = {"ready": ready, "set": set_bin, "go": go_bin}
    return out


def verify_neural_tensors(
    states: np.ndarray, inputs: np.ndarray, splits: np.ndarray
) -> None:
    """Assert the emitted tensors satisfy the downstream contract; raise on any breach."""
    if states.shape[0] != N_CONDITIONS:
        raise ValueError(f"states must have {N_CONDITIONS} conditions, got {states.shape}")
    if inputs.shape[0] != N_CONDITIONS or inputs.shape[2] != 3:
        raise ValueError(f"inputs must be [20, T, 3], got {inputs.shape}")
    if states.shape[1] != inputs.shape[1]:
        raise ValueError(
            f"states/inputs must share the time axis: {states.shape[1]} != {inputs.shape[1]}"
        )
    if not np.isfinite(states).all() or not np.isfinite(inputs).all():
        raise ValueError("states/inputs contain non-finite values")
    splits = np.asarray(splits)
    if splits.ndim != 4 or splits.shape[1] != N_CONDITIONS or splits.shape[0] < 2:
        raise ValueError(f"splits must be [S>=2, 20, T, U], got {splits.shape}")
    if not np.isfinite(splits).all():
        raise ValueError("splits contain non-finite values")
    log.info(
        "verify OK: states%s inputs%s splits%s all finite",
        states.shape, inputs.shape, splits.shape,
    )


