"""Build DMFC neural tensors from DANDI 000130.  [FOUNDATION -> plan 1.D]

Runs in the ISOLATED ingestion env (dandi/pynwb/nlb_tools); writes plain numpy
tensors to ``data/processed/`` that the modeling env reads. Modeling code never
imports pynwb/dandi. The heavy stack (nlb_tools/dandi/pynwb) is imported LAZILY
inside the I/O functions, so this module imports fine in the modeling/contracts env
too (numpy + the torch-free ``src.conditions`` / ``src.training.config`` only).

Verified dataset facts (NLB DMFC_RSG): 1 monkey (H), 1 session, 54 sorted units
(40 held-in / 14 held-out -- we use ALL 54, not the NLB forecasting split), ~1,289
trials, events Ready/Set/Go, behavioral tp. 40 real conditions = prior 2 x ts 5 x
effector 2 x direction 2; we MARGINALIZE the 2 target directions -> the 20-condition
comparison set (prior x ts x effector), matching ``src.conditions``.

OUTPUTS (into ``out_dir``, default ``data/processed/``)
    dmfc_rsg.npy         states  [20, T, U]   condition-averaged rates, U=54.
    dmfc_inputs.npy      inputs  [20, T, 3]   external drive, model's 3-channel encoding.
    dmfc_rsg_splits.npy  splits  [S, 20, T, U] disjoint trial halves -> RSA noise ceiling.
    dmfc_meta.json       provenance: per-cond trial counts, mean tp, event bins, pins.

ALIGNMENT (decision: match the model's fixed canvas). Each trial is aligned to Ready
and placed on the model's fixed ``total_time`` canvas at ``bin_ms`` bins, Ready fixed
at ``round(ready_onset/bin_ms)``. So T == round(total_time/bin_ms) is uniform across
conditions and Ready/Set land at the SAME fractional positions as the model after the
shared ``preprocess`` resample. The 3-channel input rep is generated by the model's
OWN ``src.task.rsg.build_trial`` (same constants + scale), so ``idsa.input_dsa``'s
shared-input-dim/scale requirement holds by construction.

VERIFY ON LOAD (do not assume): ts values and effector labels match src.conditions;
both effectors and both priors present; per-cell trial counts logged; thin cells
flagged. Exact ``trial_info`` column names/units are DISCOVERED and asserted here.
"""

