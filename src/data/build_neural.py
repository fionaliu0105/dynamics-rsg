def build_neural(
    out_dir: Path = PROCESSED_DIR,
    download_dir: Path = Path("data/raw"),
    dandiset_id: str = DANDISET_ID,
    bin_ms: float = DEFAULT_BIN_MS,
    smooth_ms: float = DEFAULT_SMOOTH_MS,
    config: Optional[Config] = None,
    n_splits: int = N_SPLITS,
    seed: int = 0,
) -> None:
    """Ingest DMFC_RSG -> data/processed/. Lazy-imports the ingestion stack."""
    try:
        import nlb_tools  # noqa: F401
    except ImportError as e:
        raise ImportError(
            "build_neural needs the ingestion env (nlb_tools/dandi/pynwb). "
            "See requirements-ingestion.txt."
        ) from e

    cfg = config or Config()
    out_dir = Path(out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    rng = np.random.default_rng(seed)

    nwb_path = _ensure_download(dandiset_id, Path(download_dir))
    dataset, rate_field, unit_cols = _load_rates(nwb_path, bin_ms, smooth_ms)

    trials = resolve_trials(dataset.trial_info)
    ready_col = _pick_column(list(dataset.trial_info.columns), "ready")
    arrays, has_data = _aligned_trial_arrays(
        dataset, rate_field, ready_col, cfg, bin_ms, dataset.trial_info
    )

    keep = np.where(trials["keep"] & has_data)[0]
    cond_idx, direction, tp = trials["cond_idx"], trials["direction"], trials["tp"]

    states = average_conditions(arrays, cond_idx, direction, keep)
    split_idx = stratified_splits(cond_idx, keep, n_splits, rng)
    splits = np.stack(
        [average_conditions(arrays, cond_idx, direction, s) for s in split_idx], axis=0
    )
    inputs = build_input_tensor(cfg, bin_ms)

    verify_neural_tensors(states, inputs, splits)

    counts = {c.key: int((cond_idx[keep] == condition_index(c)).sum()) for c in CONDITIONS}
    mean_tp = {
        c.key: float(np.nanmean(tp[keep][cond_idx[keep] == condition_index(c)]))
        for c in CONDITIONS
    }
    _log_counts(counts)

    np.save(out_dir / "dmfc_rsg.npy", states)
    np.save(out_dir / "dmfc_inputs.npy", inputs)
    np.save(out_dir / "dmfc_rsg_splits.npy", splits)
    _write_meta(out_dir, cfg, bin_ms, smooth_ms, unit_cols, counts, mean_tp, nwb_path)
    log.info("wrote dmfc_rsg / dmfc_inputs / dmfc_rsg_splits / dmfc_meta to %s", out_dir)


def _log_counts(counts: Dict[str, int], thin: int = 5) -> None:
    total = sum(counts.values())
    thin_cells = {k: v for k, v in counts.items() if v < thin}
    log.info("per-condition trial counts (total %d): %s", total, counts)
    if thin_cells:
        log.warning("THIN cells (< %d trials): %s -- flagged, not dropped", thin, thin_cells)


def _git_sha() -> str:
    try:
        return subprocess.check_output(
            ["git", "rev-parse", "--short", "HEAD"], text=True
        ).strip()
    except Exception:
        return "unknown"


def _write_meta(
    out_dir: Path, cfg: Config, bin_ms: float, smooth_ms: float,
    unit_cols, counts: Dict[str, int], mean_tp: Dict[str, float], nwb_path: Path,
) -> None:
    meta = {
        "dandiset": DANDISET_ID,
        "nwb_file": str(nwb_path.name),
        "n_units": len(unit_cols),
        "bin_ms": bin_ms,
        "smooth_ms": smooth_ms,
        "canvas_bins": canvas_len(cfg, bin_ms),
        "total_time_ms": cfg.total_time,
        "ready_onset_ms": cfg.ready_onset,
        "alignment": "ready-aligned on the model's fixed total_time canvas",
        "direction_marginalized": True,
        "trial_counts": counts,
        "mean_tp_ms": mean_tp,
        "event_bins": condition_event_bins(cfg, bin_ms, mean_tp),
        "git_sha": _git_sha(),
    }
    (out_dir / "dmfc_meta.json").write_text(json.dumps(meta, indent=2, sort_keys=True))


