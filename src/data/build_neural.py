def _ensure_download(dandiset_id: str, download_dir: Path) -> Path:
    """Download the dandiset if absent; return the path to its NWB file."""
    download_dir.mkdir(parents=True, exist_ok=True)
    existing = sorted(download_dir.glob(f"{dandiset_id}/**/*.nwb"))
    if not existing:
        log.info("downloading DANDI:%s into %s", dandiset_id, download_dir)
        subprocess.run(
            ["dandi", "download", f"DANDI:{dandiset_id}"],
            cwd=str(download_dir), check=True,
        )
        existing = sorted(download_dir.glob(f"{dandiset_id}/**/*.nwb"))
    if not existing:
        raise FileNotFoundError(f"no .nwb found under {download_dir}/{dandiset_id}")
    # Prefer the NLB 'train' file: the 'test' file has withheld/NaN-masked data (units
    # and behavioral tp are held out for the competition), which we do not want here.
    train = [p for p in existing if "train" in p.name.lower()]
    chosen = train[0] if train else existing[0]
    log.info("using NWB file %s", chosen)
    return chosen


def _load_rates(nwb_path: Path, bin_ms: float, smooth_ms: float):
    """Load the NWB, rebin to ``bin_ms``, Gaussian-smooth -> (dataset, rate_field, unit_cols).

    ``split_heldout=False`` keeps all 54 units in one 'spikes' field (we are not doing
    the NLB held-in/held-out forecasting split).
    """
    from nlb_tools.nwb_interface import NWBDataset

    dataset = NWBDataset(str(nwb_path), split_heldout=False)
    dataset.resample(int(bin_ms))
    # parallelized=False: macOS multiprocessing spin-up is slow/fragile and we want a
    # deterministic single-process path (AGENTS "no interactive-only assumptions").
    dataset.smooth_spk(smooth_ms, signal_type="spikes", name="smth", parallelized=False)
    rate_field = "spikes_smth"
    unit_cols = list(dataset.data[rate_field].columns)
    log.info("loaded %d units; binned to %d ms, smoothed %g ms", len(unit_cols), bin_ms, smooth_ms)
    return dataset, rate_field, unit_cols


def _aligned_trial_arrays(
    dataset, rate_field: str, ready_col: str, cfg: Config, bin_ms: float, trial_info
) -> Tuple[List[np.ndarray], np.ndarray]:
    """Ready-aligned per-trial rate arrays on the fixed canvas, ROW-ALIGNED to trial_info.

    Uses ``make_trial_data`` with the model's canvas window ``(-ready_onset,
    total_time - ready_onset)`` so every trial yields exactly ``T = canvas_len`` bins,
    reindexed onto the align-time grid (out-of-range bins -> NaN). ``make_trial_data``
    drops some trials (overlap/no-data), so we key arrays by ``trial_id`` and return a
    list positionally aligned to ``trial_info`` rows plus a ``has_data`` mask -- the
    callers index arrays by trial_info row exactly like ``cond_idx``/``direction``.
    """
    T = canvas_len(cfg, bin_ms)
    lo = -cfg.ready_onset
    # make_trial_data adds the range to the align_field timedelta, so the offsets must
    # be timedelta64 (not float ms).
    lo_td = np.timedelta64(int(round(lo)), "ms")
    hi_td = np.timedelta64(int(round(cfg.total_time - cfg.ready_onset)), "ms")
    td = dataset.make_trial_data(
        align_field=ready_col, align_range=(lo_td, hi_td), allow_nans=True,
    )
    align_ms = (td["align_time"] / np.timedelta64(1, "ms")).to_numpy()
    bin_idx = np.round((align_ms - lo) / bin_ms).astype(int)
    rates = td[rate_field].to_numpy(dtype=float)  # [rows, U]
    U = rates.shape[1]

    by_tid: Dict[int, np.ndarray] = {}
    for tid, grp in td.groupby("trial_id").indices.items():
        rows = np.asarray(grp)
        canvas = np.full((T, U), np.nan, dtype=float)
        b = bin_idx[rows]
        ok = (b >= 0) & (b < T)
        canvas[b[ok]] = rates[rows][ok]
        by_tid[int(tid)] = canvas

    tids = trial_info["trial_id"].to_numpy()
    arrays: List[np.ndarray] = []
    has_data = np.zeros(len(tids), dtype=bool)
    for i, tid in enumerate(tids):
        canvas = by_tid.get(int(tid))
        if canvas is None:
            arrays.append(np.full((T, U), np.nan, dtype=float))
        else:
            arrays.append(canvas)
            has_data[i] = True
    return arrays, has_data


